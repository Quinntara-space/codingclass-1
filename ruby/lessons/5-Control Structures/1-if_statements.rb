# If statements

# The basics of programming require us to ask questions
# and based on true or false from those questions, we can
# perform certain actions.

# For if statements we have:

# if (true)
#   do this thing
# end

# Let's define speeds of a car and the speed limit:

car_speed = 60
speed_limit = 50

# Now let's compare the speed and speed limit
# and print out the appropriate message

if car_speed < speed_limit
  puts "Print an appropriate message"
end

if car_speed > speed_limit
  puts "Print an appropriate message"
end