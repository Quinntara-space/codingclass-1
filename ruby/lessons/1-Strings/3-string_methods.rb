# Methods (Functions)
# Methods are applied to a string with
# string.method 

# Create a string variable with your name:
name = "Mr. Barrette"
puts "My name is #{name}"

# Uppercase a string:
puts "My uppercased name is #{name.upcase}"

# name.upcase! is equivalent to:
# name = name.upcase

# Turn a string in to lowercase:
puts "My lowercased name is #{name.downcase}"

# Reverse a string:
puts "My reversed name is #{name.reverse}"

