# Special characters for Whitespace:
# \n newline 
# \t tab 
# \s space

# Use a newline in a string:
puts "This\nIs\nA\nhorrible\nway\nto\nwrite\nA\nsentence\n"

# Use a tab in a string:
puts "\tFour scores and seven years ago,\nour forefathers..."

# Use a space in a string:
puts "This\sis\sa\shorrible\sway\sto\swrite\sA\ssentence\s"
